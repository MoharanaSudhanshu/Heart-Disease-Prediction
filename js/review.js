let rating = 0;

// Star Rating
const stars = document.querySelectorAll(".star");

if (stars.length > 0) {
  stars.forEach((star) => {
    star.addEventListener("click", () => {
      rating = Number(star.dataset.value);

      stars.forEach((s) => s.classList.remove("active"));

      for (let i = 0; i < rating; i++) {
        stars[i].classList.add("active");
      }
    });
  });
}

// Load reviews only if container exists
loadReviews();

// Review Form
const reviewForm = document.getElementById("reviewForm");

if (reviewForm) {
  reviewForm.addEventListener("submit", function (e) {
    e.preventDefault();

    const review = {
      name: document.getElementById("name").value,
      comment: document.getElementById("comment").value,
      rating: rating,
    };

    let reviews = JSON.parse(localStorage.getItem("reviews")) || [];

    reviews.push(review);

    localStorage.setItem("reviews", JSON.stringify(reviews));

    reviewForm.reset();

    rating = 0;

    stars.forEach((s) => s.classList.remove("active"));

    loadReviews();
  });
}

// Display Reviews
function loadReviews() {
  const reviewsContainer = document.getElementById("reviews");

  if (!reviewsContainer) return;

  const reviews = JSON.parse(localStorage.getItem("reviews")) || [];

  reviewsContainer.innerHTML = "";

  reviews.forEach((review) => {
    reviewsContainer.innerHTML += `
      <div class="review-item">
        <h4>${review.name}</h4>
        <p>${"⭐".repeat(review.rating)}</p>
        <p>${review.comment}</p>
      </div>
    `;
  });
}

// Clear Reviews Function
function clearReviews() {
  localStorage.removeItem("reviews");
  loadReviews();
}

// Download Report
function downloadReport() {
  const patient = JSON.parse(localStorage.getItem("patientData"));

  if (!patient) {
    alert("No patient data found.");
    return;
  }

  let report = `
Heart Disease Prediction Report

Age: ${patient.age}
Gender: ${patient.sex}

Blood Pressure: ${patient.trestbps}

Cholesterol: ${patient.chol}

Heart Rate: ${patient.thalach}

Generated By:
HeartAI System
`;

  const blob = new Blob([report], {
    type: "text/plain",
  });

  const url = window.URL.createObjectURL(blob);

  const a = document.createElement("a");

  a.href = url;
  a.download = "Heart_Report.txt";

  document.body.appendChild(a);

  a.click();

  document.body.removeChild(a);

  window.URL.revokeObjectURL(url);
}
